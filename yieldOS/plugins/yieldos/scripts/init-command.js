#!/usr/bin/env node
'use strict';

const fs = require('node:fs');
const os = require('node:os');
const path = require('node:path');
const {
  DEFAULT_PROFILES,
  PREVENTIVE_LEARNING_INSTRUCTION,
  PROFILE_SECTIONS,
} = require('./init-profiles');

const VALID_AGENTS = new Set(['claude', 'codex', 'both']);
const VALID_SCOPES = new Set(['project', 'local', 'personal', 'org']);

function parseArgs(argv = []) {
  const parsed = {
    agent: 'both',
    scope: 'project',
    profiles: [...DEFAULT_PROFILES],
    write: false,
    force: false,
    help: false,
    list: false,
  };

  const args = [...argv];
  if (args[0] === 'list') {
    parsed.list = true;
    args.shift();
  }

  while (args.length > 0) {
    const arg = args.shift();
    if (arg === '--agent') {
      parsed.agent = requireValue(arg, args.shift());
    } else if (arg === '--scope') {
      parsed.scope = requireValue(arg, args.shift());
    } else if (arg === '--profile' || arg === '--profiles') {
      parsed.profiles = splitProfiles(requireValue(arg, args.shift()));
    } else if (arg === '--write') {
      parsed.write = true;
    } else if (arg === '--force') {
      parsed.force = true;
    } else if (arg === '--help' || arg === '-h') {
      parsed.help = true;
    } else {
      throw new Error(`unknown init option: ${arg}`);
    }
  }

  validateParsed(parsed);
  return parsed;
}

function requireValue(flag, value) {
  if (!value || value.startsWith('--')) throw new Error(`${flag} needs a value`);
  return value;
}

function splitProfiles(value) {
  return value.split(',').map((item) => item.trim()).filter(Boolean);
}

function validateParsed(parsed) {
  if (!VALID_AGENTS.has(parsed.agent)) throw new Error('--agent must be claude, codex, or both');
  if (!VALID_SCOPES.has(parsed.scope)) throw new Error('--scope must be project, local, personal, or org');
  if (parsed.scope === 'local' && parsed.agent !== 'claude') {
    throw new Error('--scope local only supports --agent claude');
  }
  if (parsed.profiles.length === 0) throw new Error('--profile needs at least one profile');
  for (const profile of parsed.profiles) {
    if (!PROFILE_SECTIONS[profile]) throw new Error(`unknown profile: ${profile}`);
  }
}

function renderInstructionFiles(options = {}) {
  const parsed = {
    agent: options.agent || 'both',
    scope: options.scope || 'project',
    profiles: options.profiles || DEFAULT_PROFILES,
  };
  validateParsed({ ...parsed, write: false, force: false, help: false, list: false });

  const shared = renderSharedInstructions(parsed);
  if (parsed.agent === 'claude') return [{ path: 'CLAUDE.md', content: shared }];
  if (parsed.agent === 'codex') return [{ path: 'AGENTS.md', content: shared }];
  if (parsed.scope !== 'project') {
    return [
      { path: 'AGENTS.md', content: shared },
      { path: 'CLAUDE.md', content: shared },
    ];
  }
  return [
    { path: 'AGENTS.md', content: shared },
    { path: 'CLAUDE.md', content: renderClaudeImport() },
  ];
}

function renderSharedInstructions(parsed) {
  const sections = parsed.profiles.flatMap((profile) => {
    const section = PROFILE_SECTIONS[profile];
    return [
      `## ${section.title}`,
      '',
      ...section.bullets.map((bullet) => `- ${bullet}`),
      '',
    ];
  });

  return [
    '# Agent Instructions',
    '',
    `Generated by yieldOS for ${scopeLabel(parsed.scope)} scope.`,
    '',
    '## Operating contract',
    '',
    '- Follow system and user instructions first; use repository instructions and these safety defaults only when they do not conflict.',
    `- ${PREVENTIVE_LEARNING_INSTRUCTION}`,
    '- Keep changes small, explain security-relevant tradeoffs, and preserve existing project patterns.',
    '- If an instruction conflicts with an explicit user request, surface the conflict before acting.',
    '',
    ...sections,
  ].join('\n');
}

function renderClaudeImport() {
  return [
    '@AGENTS.md',
    '',
    '## Claude Code',
    '',
    '- Treat `AGENTS.md` as the shared yieldOS instruction source for this project.',
    '- Use `/yieldos:update` to update yieldOS and `/yieldos:audit` when explicit source-code review is needed.',
    '',
  ].join('\n');
}

function scopeLabel(scope) {
  return {
    project: 'shared project',
    local: 'local project',
    personal: 'personal full',
    org: 'organization',
  }[scope] || scope;
}

function runInit(projectRoot, argv = process.argv.slice(2), options = {}) {
  let parsed;
  try {
    parsed = parseArgs(argv);
  } catch (err) {
    return { exitCode: 2, message: `yieldOS init error: ${err.message}` };
  }

  if (parsed.help) return { exitCode: 0, message: usage() };
  if (parsed.list) return { exitCode: 0, message: listProfiles() };
  if (parsed.write && parsed.scope === 'org') {
    return { exitCode: 2, message: 'yieldOS init org scope is export-only. Copy the generated managed instructions into your org deployment system.' };
  }

  const files = resolveTargets(projectRoot, parsed, options);
  if (!parsed.write) return { exitCode: 0, message: renderPreview(projectRoot, files, parsed), files };

  const existing = files.filter((file) => fs.existsSync(file.absolutePath));
  if (existing.length > 0 && !parsed.force) {
    return {
      exitCode: 2,
      message: `yieldOS init refused to overwrite because file already exists: ${displayPath(projectRoot, existing[0].absolutePath)}. Rerun with --force to replace it.`,
    };
  }

  for (const file of files) {
    fs.mkdirSync(path.dirname(file.absolutePath), { recursive: true });
    fs.writeFileSync(file.absolutePath, file.content);
  }

  return {
    exitCode: 0,
    message: [
      'yieldOS init wrote instruction files:',
      ...files.map((file) => `- ${displayPath(projectRoot, file.absolutePath)}`),
    ].join('\n'),
    files,
  };
}

function resolveTargets(projectRoot, parsed, options = {}) {
  const home = options.home || os.homedir();
  return renderInstructionFiles(parsed).map((file) => ({
    ...file,
    absolutePath: targetPath(projectRoot, home, parsed.scope, file.path),
  }));
}

function targetPath(projectRoot, home, scope, filename) {
  if (scope === 'personal') {
    if (filename === 'CLAUDE.md') return path.join(home, '.claude', 'CLAUDE.md');
    return path.join(home, '.codex', 'AGENTS.md');
  }
  if (scope === 'local') return path.join(projectRoot, 'CLAUDE.local.md');
  return path.join(projectRoot, filename);
}

function renderPreview(projectRoot, files, parsed = {}) {
  const footer = parsed.scope === 'org'
    ? 'Organization scope is export-only. Copy these generated instructions into your managed rollout system.'
    : 'Rerun with --write to create these files.';
  return [
    'yieldOS init preview',
    '',
    ...files.flatMap((file) => [
      `--- ${displayPath(projectRoot, file.absolutePath)} ---`,
      file.content.trimEnd(),
      '',
    ]),
    footer,
  ].join('\n');
}

function displayPath(projectRoot, absolutePath) {
  const relative = path.relative(projectRoot, absolutePath);
  return relative && !relative.startsWith('..') ? relative : absolutePath;
}

function listProfiles() {
  return Object.entries(PROFILE_SECTIONS)
    .map(([id, section]) => `${id} - ${section.title}`)
    .join('\n');
}

function usage() {
  return [
    'Usage: yieldos-init [list] [--agent claude|codex|both] [--scope project|local|personal|org] [--profile a,b] [--write] [--force]',
    '',
    `Default profiles: ${DEFAULT_PROFILES.join(',')}`,
    'Default mode previews files. Add --write to create them.',
  ].join('\n');
}

function main() {
  const result = runInit(process.cwd());
  process.stdout.write(`${result.message}\n`);
  process.exit(result.exitCode);
}

if (require.main === module) main();

module.exports = {
  DEFAULT_PROFILES,
  PROFILE_SECTIONS,
  parseArgs,
  renderInstructionFiles,
  runInit,
  usage,
};
